public class Guerreiro {
    private int espada;
    private int escudo;

    public Guerreiro(int espada, int escudo) {
        this.espada = espada;
        this.escudo = escudo;
    }

    public int getEspada() {
        return espada;
    }

    public void setEspada(int espada) {
        this.espada = espada;
    }

    public int getEscudo() {
        return escudo;
    }

    public void setEscudo(int escudo) {
        this.escudo = escudo;
    }
}
